// 06. 연산자

/*
    산술 연산자
    +, -, *, /, %

    대입 연산자
    좌변 = 우변 (우변값이 좌변에 들어간다)
    var num = 20

    복합 대입 연산자
    +=, -= , *=, /=, %=
    a += 10 ==> a = a + 10

    증감 연산자
    ++, --
    a++ ==> a = a + 1

    비교 연산자
    >, >=, <, <=, ==, !=
    True == True ==> True
    False == False ==> False
    True != True ==> False
    True != False ==> True

    논리 연산자
    &&, ||, !
    True && True ==> True
    True || False ==> True
    !True ==> False
    !False ==> True
 */